#Int
n1 = int(input('Digite um número seu bosta: '))
n2 = int(input('Digite o segundo número seu bosta: '))

soma = n1 + n2

print('A soma dos números {} e {} é {} entendeu? loko!'.format(n1, n2, soma))

#Float
n3 = float(input('Digite um número quebrado seu bosta: '))
